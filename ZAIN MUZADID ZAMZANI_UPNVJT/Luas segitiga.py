# menerima inputan dari user dan mengubahnya ke integer
alas = int(input("Masukan alas : "))
tinggi = int(input("Masukan tinggi : "))

# menghitung luas segitiga dari dan mengubahnya ke float
# untuk mendapatkan hasil desimal
luas = float(alas*tinggi/2)

# menampilkan hasil dari perhitungan luas
print(f"Jadi luas dari segitiga tersebut adalah {luas}")
